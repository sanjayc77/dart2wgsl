import 'dart:io';
import 'package:args/args.dart';
import 'package:path/path.dart' as p;
import 'package:dart2wgsl/src/parser.dart';
import 'package:dart2wgsl/src/validator.dart';
import 'package:dart2wgsl/src/transpiler.dart';

void main(List<String> arguments) async {
  final parser = ArgParser()
    ..addOption('output', abbr: 'o', help: 'Path to write the transpiled WGSL or WGSL Dart file')
    ..addFlag('help', abbr: 'h', negatable: false, help: 'Print this usage information');

  try {
    final results = parser.parse(arguments);

    if (results['help'] == true || results.rest.isEmpty) {
      print('Usage: dart2wgsl <input_file.dart> [options]');
      print(parser.usage);
      exit(results['help'] == true ? 0 : 1);
    }

    final inputPath = results.rest.first;
    var outputPath = results['output'] as String?;

    if (outputPath == null) {
      // By default, replace .shader.dart or .dart with .wgsl.dart next to the input
      if (inputPath.endsWith('.shader.dart')) {
        outputPath = inputPath.replaceAll('.shader.dart', '.wgsl.dart');
      } else if (inputPath.endsWith('.dart')) {
        outputPath = inputPath.replaceAll('.dart', '.wgsl.dart');
      } else {
        outputPath = '$inputPath.wgsl.dart';
      }
    }

    print('Reading and resolving $inputPath...');
    final rootLibrary = await resolveFile(inputPath);

    print('Crawling dependencies...');
    final units = await crawlLibraryAsts(rootLibrary);

    print('Validating shader code compatibility...');
    final errors = ShaderValidator.validate(units);
    if (errors.isNotEmpty) {
      stderr.writeln('❌ Validation errors found in shader code:');
      for (final error in errors) {
        stderr.writeln('  $error');
      }
      exit(1);
    }

    print('Transpiling to WGSL...');
    final wgslSource = ShaderTranspiler.transpile(units);

    // Format output as a Dart file with a static raw string constant
    final baseName = p.basenameWithoutExtension(outputPath);
    final varName = baseName.endsWith('.wgsl') 
        ? '${baseName.replaceAll('.wgsl', '')}Shader'
        : '${baseName}Shader';

    // Normalize variable name to camelCase (e.g. toy_shader -> toyShader)
    final normalizedVarName = varName
        .split(RegExp(r'[^a-zA-Z0-9]'))
        ..removeWhere((s) => s.isEmpty);
    
    String finalVarName = 'shaderSource';
    if (normalizedVarName.isNotEmpty) {
      finalVarName = normalizedVarName.first.toLowerCase() + 
          normalizedVarName.skip(1).map((s) => s[0].toUpperCase() + s.substring(1)).join();
    }

    String outputContent;
    if (outputPath.endsWith('.wgsl')) {
      // If the user explicitly requested a .wgsl file, write raw WGSL
      outputContent = wgslSource;
    } else {
      // Otherwise, write a statically importable .dart file
      outputContent = '''
// Generated by dart2wgsl. Do not modify.

const String $finalVarName = r"""
$wgslSource""";
''';
    }

    final outputFile = File(outputPath);
    await outputFile.parent.create(recursive: true);
    await outputFile.writeAsString(outputContent);

    print('Successfully generated: $outputPath');
  } catch (e) {
    stderr.writeln('❌ Error: $e');
    exit(1);
  }
}
